namespace Popsql.Text
{
	/// <summary>
	/// Provides <see cref="SqlKeyword"/> instances for well-known SQL keywords.
	/// </summary>
	public static class SqlKeywords
	{
		/// <summary>
		/// Represents the SQL SELECT keyword.
		/// </summary>
		public static readonly SqlKeyword Select = "SELECT";
		/// <summary>
		/// Represents the SQL FROM keyword.
		/// </summary>
		public static readonly SqlKeyword From = "FROM";
		/// <summary>
		/// Represents the SQL WHERE keyword.
		/// </summary>
		public static readonly SqlKeyword Where = "WHERE";
	}
}